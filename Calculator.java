public class Calculator 

{

	/**

	 * A method that adds two integers together.

	 * @param a

	 * @param b

	 * @return

	 */

	public int add(int a, int b)

	{

		return a + b;

	}

	

	public float divide(int a, int b)

	{

		return a / b;

	}

	

	public int subtract(int a, int b)

	{

		return a - b;

	}

	

	public int multiply(int a, int b)

	{

		return a * b;

	}
	

}
